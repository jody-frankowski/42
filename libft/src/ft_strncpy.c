#include <string.h>

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char *save;

	save = dst;
	while (*src && len--)
		*dst++ = *src++;
	if (!*src)
		while (len--)
			*dst++ = '\0';
	return (save);
}
