#include <string.h>
#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	void *save;

	save = dst;
	while (n--)
		*(unsigned char*)dst++ = *(unsigned char*)src++;
	return (save);
}
