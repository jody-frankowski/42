#include <stdlib.h>
#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *elem;
	t_list *next;

	if (!alst || !*alst || !del)
		return ;
	elem = *alst;
	while (elem)
	{
		next = elem->next;
		del(elem->content, elem->content_size);
		free(elem);
		elem = next;
	}
	*alst = NULL;
}
