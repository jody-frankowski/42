#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dup;
	size_t	len;

	len = ft_strlen(s1);
	if (!(dup = malloc(sizeof(*s1) * (len + 1))))
		return (NULL);
	return (ft_strcpy(dup, s1));
}
