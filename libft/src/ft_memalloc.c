#include <stdlib.h>
#include <string.h>

void	*ft_memalloc(size_t size)
{
	void	*ptr;
	size_t	i;

	if (!(ptr = malloc(size)))
		return (NULL);
	i = 0;
	while (i < size)
		((char *)ptr)[i++] = 0;
	return (ptr);
}
