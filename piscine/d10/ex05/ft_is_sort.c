int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	if (!f || !tab)
		return (0);
	while (length--)
		if (*(tab + 1) && f(*tab, *(tab + 1)) > 0)
			return (0);
	return (1);
}
