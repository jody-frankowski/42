int	match(char *s1, char *s2)
{
	if (!*s1 && !*s2)
		return (1);
	if (*s2 && *s2 == '*')
		return ((*s1 && match(s1 + 1, s2)) || (*s2 && match(s1, s2 + 1)));
	if (*s1 != *s2)
		return (0);
	return (match(s1 + 1, s2 + 1));
}
